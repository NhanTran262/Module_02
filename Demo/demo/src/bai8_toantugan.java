public class bai8_toantugan {
    public static void main(String[] args) {
        //gán cộng
        int x = 8;
        x+=5;
        System.out.println("x= " +x);

        //gán trừ
        int x2 = 8;
        x2-=5;
        System.out.println("x2= " +x2);

        int a = 7;
        int b = 9;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);

    }
}
